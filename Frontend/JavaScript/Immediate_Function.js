//  What do you mean by Immediate Functions / Self Invoking Functions ?
//  Immediate functions are the functions that executes as soon as it is defined.

//  Creating an immediate function is simple: you add the open / close parentheses 
//  after the closing curly bracket, and then wrap the entire function in parentheses.

